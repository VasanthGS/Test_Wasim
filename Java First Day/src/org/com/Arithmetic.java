package org.com;

public class Arithmetic {
	public static void main(String[] args) {
		int a,b, result;
		a=10;
		b=20;

		result = a+b;
		System.out.println(result);

		result = a-b;
		System.out.println(result);

		result = a*b;
		System.out.println(result);

		result = a/b;
		System.out.println(result);
	}

}
